    private void btnFind1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFind1ActionPerformed
        String id = txtDriverID.getText();
        if (id.length() > 0) {
            List<Driver> list = new ArrayList<>();
            for (Driver driver : drivers) {
                if ((driver.getId() + "").matches(".*" + id + ".*")) {
                    list.add(driver);
                }
            }
            drivers.clear();
            drivers.addAll(list);
            if (list.size() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Not found driver !");
            } else {
                this.showDrivers();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Please enter ID driver");
        }
    }//GEN-LAST:event_btnFind1ActionPerformed

    private void btnFind2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFind2ActionPerformed
        String id = txtBusID.getText();
        if (id.length() > 0) {
            List<Way> list = new ArrayList<>();
            for (Way way : buses) {
                if ((way.getId() + "").matches(".*" + id + ".*")) {
                    list.add(way);
                }
            }
            buses.clear();
            buses.addAll(list);
            if (list.size() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Not found bus !");
            } else {
                this.showBuses();
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Please enter ID way");
        }
    }//GEN-LAST:event_btnFind2ActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int busIndex = tblBuses.getSelectedRow();
        int driverIndex = tblDriver.getSelectedRow();
        
        if(busIndex == -1 || driverIndex == -1) {
            JOptionPane.showMessageDialog(rootPane, "Please choose driver and bus");
        } else {
            Driver driver = drivers.get(driverIndex);
            Way bus = buses.get(busIndex);
            int turn = Integer.parseInt(comboTurn.getSelectedItem().toString());
            Pair<Way, Integer> pair = new Pair(bus, turn);
            boolean status = home.addManager(driver, pair);
            if(status == false) {
                JOptionPane.showMessageDialog(rootPane, "Total turn of driver can not over 15\nPlease check again!");
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

