    private void showBuses() {
        modelDrivers.setRowCount(0);
        int i = 0;
        for (Driver driver : drivers) {
            modelDrivers.addRow(new Object[]{
                i++, driver.getId(), driver.getName()
            });
        }
    }

    private void showDrivers() {
        modelBus.setRowCount(0);
        int i = 0;
        for (Way way : buses) {
            modelBus.addRow(new Object[]{
                i++, way.getId(), way.getDistance(), way.getStation()
            });
        }
    }
