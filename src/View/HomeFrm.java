    boolean addManager(Driver dr, Pair<Way, Integer> pair) {
        boolean isAccepted = controller.isAccepted(listManager, dr, pair);
        if(!isAccepted){
            return false;
        }
        controller.addOrUpdate(listManager, dr, pair);
        this.showData(listManager, modelManager);
        controller.writeToFile(listManager, "JOB.TXT");
        return true;
    }

    public void addDriver(Driver d) {
        listDr.add(d);
        this.showData(listDr, modelDr);
        controller.writeToFile(listDr, "LX.TXT");
        showDr();
        showBuses();
    }
    
    public void addBuses(Way t){
        listBuses.add(t);
        this.showData(listBuses, modelBuses);
        controller.writeToFile(listBuses, "WAY.TXT");
    }

