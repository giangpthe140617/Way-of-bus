    @Override
    public <T> void showData(List<T> list, DefaultTableModel model) {
        model.setRowCount(0);
        for (T t : list) {
            if (t instanceof Driver) {
                Driver d = (Driver) t;
                model.addRow(new Object[]{
                    d.getId(), d.getName(), d.getAddress(), d.getLevel()
                });
            }
            if(t instanceof Manager) {
                Manager s = (Manager) t;
                for (Pair<Way, Integer> pair : s.getPairs()) {
                    model.addRow(new Object[]{
                        s.getDriver().getId(), s.getDriver().getName(),
                        pair.getWay().getId(), pair.getTurn(), 
                        s.getTotalTurn(), s.getPairs().size()
                    });
                }
            }
        }
    }

    private void showDr() {
        listDr = controller.readDataFromFile("LX.TXT");
        if (listDr.size() > 0) {
            int mId = listDr.get(listDr.size() - 1).getId();
            Driver.setsId(mId + 1);
        }
        this.showData(listDr, modelDr);
    }

    private void showBuses() {
        listBuses = controller.readDataFromFile("TUYEN.TXT");
        if(listBuses.size() > 0) {
            int mId = listBuses.get(listBuses.size() - 1).getId();
            Way.setsId(mId + 1);
        }
        this.showData(listBuses, modelBuses);
    }

    private void showManager() {
        listManager = controller.readDataFromFile("JOB.TXT");
        this.showData(listManager, modelManager);
    }

    private void showRecord() {
        modelRecord.setRowCount(0);
        controller.sortByTotalDistance(listManager);
        int i = 1;
        for (Manager m : listManager) {
            modelRecord.addRow(new Object[]{
                i++, m.getDriver().getId(), m.getDriver().getName(), m.getTotalTurn(),
                m.getPairs().size(), controller.getTotalDistance(m)
            });
        }
    }
